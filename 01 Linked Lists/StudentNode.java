public class StudentNode {
    int rollNum;
    String name;
    int age;
    String grade;
    StudentNode next;
    public StudentNode(int rollNum, String name, int age, String grade){
        this.rollNum = rollNum;
        this.name = name;
        this.age = age;
        this.grade = grade;
        this.next = null;
    }
}


